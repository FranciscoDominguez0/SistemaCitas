    private void jButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarActionPerformed

         if (jTextFieldNomnbre.getText().isEmpty() ||
        jTextFieldApellido.getText().isEmpty() ||
        jTextFieldCedula.getText().isEmpty() ||
        jTextFieldTelefono.getText().isEmpty() ||
        jDateChooserFecha_cita.getDate() == null ||
        jTextFieldHora.getText().isEmpty() ||
        jTextFieldNombre_negocio.getText().isEmpty() ||
        jTextFieldDireccion.getText().isEmpty() ||
        jTextFieldnumero_empleados.getText().isEmpty() ||
        jComboBoxTipo_negocio.getSelectedIndex() == 0) {
        
        JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos.", "Campos Vac√≠os", JOptionPane.WARNING_MESSAGE);
        return;
    }

    // Obtener los datos del formulario
    String nombre = jTextFieldNomnbre.getText();
    String apellido = jTextFieldApellido.getText();
    String cedula = jTextFieldCedula.getText();
    String telefono = jTextFieldTelefono.getText();
    java.util.Date fechaCitaUtil = jDateChooserFecha_cita.getDate();
    java.sql.Date fechaCita = new java.sql.Date(fechaCitaUtil.getTime());
    String horaCita = jTextFieldHora.getText();
    String nombreNegocio = jTextFieldNombre_negocio.getText();
    String direccionNegocio = jTextFieldDireccion.getText();
    int numeroEmpleados;

    try {
        numeroEmpleados = Integer.parseInt(jTextFieldnumero_empleados.getText());
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "El n√∫mero de empleados debe ser un n√∫mero v√°lido.", "Error de Validaci√≥n", JOptionPane.ERROR_MESSAGE);
        return;
    }

    String tipoNegocio = jComboBoxTipo_negocio.getSelectedItem().toString();

    // Insertar en la base de datos
    String sql = "INSERT INTO AvisoOperaciones (nombre, apellido, cedula, telefono, fecha_cita, hora_cita, nombre_negocio, tipo_negocio, direccion_negocio, numero_empleados) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?)";
    
    try (Connection conn = new conectar().conexion(); 
         PreparedStatement pst = conn.prepareStatement(sql)) {
        
        pst.setString(1, nombre);
        pst.setString(2, apellido);
        pst.setString(3, cedula);
        pst.setString(4, telefono);
        pst.setDate(5, fechaCita);
        pst.setString(6, horaCita);
        pst.setString(7, nombreNegocio);
        pst.setString(8, tipoNegocio);
        pst.setString(9, direccionNegocio);
        pst.setInt(10, numeroEmpleados);

        int rowsInserted = pst.executeUpdate();
        if (rowsInserted > 0) {
            JOptionPane.showMessageDialog(this, "Aviso de Operaciones registrado exitosamente.", "Registro Exitoso", JOptionPane.INFORMATION_MESSAGE);
            limpiarCampos(); // Limpiar los campos despu√©s de guardar
        } else {
            JOptionPane.showMessageDialog(this, "Error al registrar el Aviso de Operaciones.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error en la base de datos: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
}

