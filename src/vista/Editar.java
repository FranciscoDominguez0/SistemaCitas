    private void jButtonActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonActualizarActionPerformed
        int fila = jTableUsuarios.getSelectedRow(); 
if (fila == -1) {
    JOptionPane.showMessageDialog(null, "Por favor, selecciona una fila de la tabla.");
    return;
}

// Obtener los valores actuales de los JTextFields
String tipoTramite = jTableUsuarios.getValueAt(fila, 0).toString(); // Primera columna
String nombre = jTextFieldNombre.getText();
String apellido = jTextFieldApellido.getText();
String cedula = jTextFieldCedula.getText();
String telefono = jTextFieldTelefono.getText();

// Validar que no haya campos vacíos
if (nombre.isEmpty() || apellido.isEmpty() || cedula.isEmpty() || telefono.isEmpty()) {
    JOptionPane.showMessageDialog(null, "Por favor, completa todos los campos.");
    return;
}

// Determinar la tabla a actualizar según el tipo de trámite
String tabla = "";
switch (tipoTramite) {
    case "Trámite Placa":
        tabla = "tramiteplaca";
        break;
    case "Permiso Construcción":
        tabla = "permisoconstruccion";
        break;
    case "Aviso Operaciones":
        tabla = "avisooperaciones";
        break;
    default:
        JOptionPane.showMessageDialog(null, "Tipo de trámite desconocido.");
        return;
}

// Conexión y actualización
conectar cc = new conectar();
Connection cn = cc.conexion();
String sql = "UPDATE " + tabla + " SET nombre = ?, apellido = ?, cedula = ? WHERE telefono = ?";
try {
    PreparedStatement pst = cn.prepareStatement(sql);
    pst.setString(1, nombre);     // Actualizar "nombre"
    pst.setString(2, apellido);  // Actualizar "apellido"
    pst.setString(3, cedula);  // Actualizar "telefono"
    pst.setString(4, telefono);    // Usar "cedula" para identificar

    int actualizado = pst.executeUpdate();
    if (actualizado > 0) {
        JOptionPane.showMessageDialog(null, "Datos actualizados correctamente.");
        cargar(""); // Recargar la tabla para reflejar los cambios
    } else {
        JOptionPane.showMessageDialog(null, "No se pudo actualizar los datos.");
    }
} catch (SQLException ex) {
    JOptionPane.showMessageDialog(null, "Error al actualizar los datos: " + ex.getMessage());
}
    }//GEN-LAST:event_jButtonActualizarActionPerformed


