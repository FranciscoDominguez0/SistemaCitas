    void cargar(String valor) {
        String[] titulos = {"Tipo de Trámite", "Nombre", "Apellido", "Cédula", "Teléfono"};
        String[] registros = new String[5];
        model = new DefaultTableModel(null, titulos);

        conectar cc = new conectar();
        Connection cn = cc.conexion();

        try {
            // Consulta SQL para filtrar dinámicamente por nombre, apellido o cédula
            String sql = "SELECT 'Trámite Placa' AS tipo_tramite, nombre, apellido, cedula, telefono " +
                         "FROM tramiteplaca WHERE CONCAT(nombre, ' ', apellido, ' ', cedula) LIKE ? " +
                         "UNION " +
                         "SELECT 'Permiso Construcción' AS tipo_tramite, nombre, apellido, cedula, telefono " +
                         "FROM permisoconstruccion WHERE CONCAT(nombre, ' ', apellido, ' ', cedula) LIKE ? " +
                         "UNION " +
                         "SELECT 'Aviso Operaciones' AS tipo_tramite, nombre, apellido, cedula, telefono " +
                         "FROM avisooperaciones WHERE CONCAT(nombre, ' ', apellido, ' ', cedula) LIKE ?";

            PreparedStatement pst = cn.prepareStatement(sql);
            String filtro = "%" + valor + "%";
            pst.setString(1, filtro);
            pst.setString(2, filtro);
            pst.setString(3, filtro);

            ResultSet rs = pst.executeQuery();

            // Llenar el modelo con los datos obtenidos
            while (rs.next()) {
                registros[0] = rs.getString("tipo_tramite");
                registros[1] = rs.getString("nombre");
                registros[2] = rs.getString("apellido");
                registros[3] = rs.getString("cedula");
                registros[4] = rs.getString("telefono");
               
                model.addRow(registros);
            }

            jTableUsuarios.setModel(model);

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error al cargar los datos: " + ex.getMessage());
        }
    }
    void agregarEventoClickTabla() {
    // Agregar un MouseListener para detectar clics en las filas de la tabla
    jTableUsuarios.addMouseListener(new java.awt.event.MouseAdapter() {
        @Override
        public void mouseClicked(java.awt.event.MouseEvent evt) {
            // Obtener la fila seleccionada
            int fila = jTableUsuarios.rowAtPoint(evt.getPoint());
            if (fila != -1) {
                // Obtener los valores de las celdas de la fila seleccionada
                String nombre = jTableUsuarios.getValueAt(fila, 1).toString();
                String apellido = jTableUsuarios.getValueAt(fila, 2).toString();
                String cedula = jTableUsuarios.getValueAt(fila, 3).toString();
                String telefono = jTableUsuarios.getValueAt(fila, 4).toString();

                // Rellenar los JTextField con los valores obtenidos
                jTextFieldNombre.setText(nombre);
                jTextFieldApellido.setText(apellido);
                jTextFieldCedula.setText(cedula);
                jTextFieldTelefono.setText(telefono);
            }
        }
    });
}


